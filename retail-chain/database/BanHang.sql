-- Tạo bảng tạm để lưu trữ danh sách ManagerID
CREATE TEMPORARY TABLE tmpManagerID
SELECT ManagerID
FROM Manager
ORDER BY RAND()
LIMIT 100; -- Chọn số lượng ManagerID tùy ý

-- Thực hiện INSERT INTO với ManagerID ngẫu nhiên
INSERT INTO Bill (SaleID, ProductID, Amount, Cost, CreateDate, BillID, ManagerID)
SELECT s.SaleID,
    p.ProductID,
    FLOOR(RAND()*10)+1,
    SUM(p.Price * FLOOR(RAND()*10)+1),
    DATE_ADD(CURRENT_DATE(), INTERVAL - FLOOR(RAND() * 730) DAY) + INTERVAL FLOOR(RAND() * 86400) SECOND,
    CONCAT('Bill', FLOOR(RAND() * 100000) + 1),
    tmp.ManagerID
FROM Sales s, Products p, tmpManagerID tmp
WHERE p.Price * FLOOR(RAND()*10)+1 > 200000
GROUP BY s.SaleID, tmp.ManagerID
HAVING SUM(p.Price * FLOOR(RAND()*10)+1) > 200000
ORDER BY RAND()
LIMIT 360000;

